package bean;

public class beanCita {
	private int idcita;
	private String fechaAtencion;
	private String doctor;
	public String horaCita;
	public String detalleCita;
	public int idPaciente;
	public String celularPc;
	
	public int duracion;
	
	public int consultorio;
	
	public int getIdcita() {
		return idcita;
	}
	public void setIdcita(int idcita) {
		this.idcita = idcita;
	}
	public String getFechaAtencion() {
		return fechaAtencion;
	}
	public void setFechaAtencion(String fechaAtencion) {
		this.fechaAtencion = fechaAtencion;
	}
	public String getDoctor() {
		return doctor;
	}
	public void setDoctor(String doctor) {
		this.doctor = doctor;
	}
	public String getHoraCita() {
		return horaCita;
	}
	public void setHoraCita(String horaCita) {
		this.horaCita = horaCita;
	}
	public String getDetalleCita() {
		return detalleCita;
	}
	public void setDetalleCita(String detalleCita) {
		this.detalleCita = detalleCita;
	}
	public int getIdPaciente() {
		return idPaciente;
	}
	public void setIdPaciente(int idPaciente) {
		this.idPaciente = idPaciente;
	}
	public String getCelularPc() {
		return celularPc;
	}
	public void setCelularPc(String celularPc) {
		this.celularPc = celularPc;
	}
	public double getDuracion() {
		return duracion;
	}
	public void setDuracion(int duracion) {
		this.duracion = duracion;
	}

	public int getConsultorio() {
		return consultorio;
	}
	public void setConsultorio(int consultorio) {
		this.consultorio = consultorio;
	}
	
	
	
	

}
